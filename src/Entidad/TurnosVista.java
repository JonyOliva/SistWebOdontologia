package Entidad;

public class TurnosVista{

	Turno turno;
	String nombrePac;
	String apellidoPac;
	String nombreOd;
	String apellidoOd;
	
	
	public Turno getTurno() {
		return turno;
	}
	public void setTurno(Turno turno) {
		this.turno = turno;
	}
	public String getNombrePac() {
		return nombrePac;
	}
	public void setNombrePac(String nombrePac) {
		this.nombrePac = nombrePac;
	}
	public String getApellidoPac() {
		return apellidoPac;
	}
	public void setApellidoPac(String apellidoPac) {
		this.apellidoPac = apellidoPac;
	}
	public String getNombreOd() {
		return nombreOd;
	}
	public void setNombreOd(String nombreOd) {
		this.nombreOd = nombreOd;
	}
	
	public String getApellidoOd() {
		return apellidoOd;
	}
	
	public void setApellidoOd(String apellidoOd) {
		this.apellidoOd = apellidoOd;
	}
}
